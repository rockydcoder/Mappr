public class SingleRowData {

    private int iconId;
    private String text;
    private int rowBg;

    public int getRowBg() {
        return rowBg;
    }

    public void setRowBg(int rowBg) {
        this.rowBg = rowBg;
    }



    public int getIconId() {
        return iconId;
    }

    public void setIconId(int iconId) {
        this.iconId = iconId;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
